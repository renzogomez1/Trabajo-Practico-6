    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        Long phone = 0L;
        Cliente c;
        boolean eLanza=false;
        
        DirectorioTelefonico dt = MenuDirectorio.getD1();
        try{    
            phone = Long.valueOf(telefono.getText());
        }catch(NumberFormatException e){
            eLanza = true;
        }
        c = dt.buscarCliente(phone);
        if(c != null && !eLanza){
            nombre.setText(c.getNombre());
            dni.setText(""+c.getDni());
            direccion.setText(c.getDireccion());
            ciudad.setText(c.getCiudad());
            telefono.setText(phone+"");
            apellido.setText(c.getApellido());
        }else{
            JOptionPane.showMessageDialog(null,"El cliente no se encuentra cargado o los datos son incorrectos, intente nuevamente.");
        }

    }//GEN-LAST:event_buscarActionPerformed

